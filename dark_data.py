def pick_pickup(area=None, her_presence=0):
    """随机捡一个碎片。返回碎片dict或None。"""
    roll = random.random()
    # 基础50%概率捡到东西
    if roll > 0.50:
        return None
    # 决定捡到什么
    roll2 = random.random()
    # 坏碎片 15%
    if roll2 < 0.15:
        return random.choice(PICKUPS["bad"])
    # 她的物件
    her_chance = 0.05 + her_presence * 0.03
    if roll2 < 0.15 + her_chance:
        return random.choice(PICKUPS["her_things"])
    # 前人遗物 20%
    if roll2 < 0.15 + her_chance + 0.20:
        return random.choice(PICKUPS["relics"])
    # 感觉碎片 25%
    if roll2 < 0.15 + her_chance + 0.20 + 0.25:
        return random.choice(PICKUPS["senses"])
    # 残页 20%
    if roll2 < 0.15 + her_chance + 0.20 + 0.25 + 0.20:
        page = random.choice(PICKUPS["pages"])
        return {"name": "残页", "desc": page["text"], "author": page.get("author", "无名"), "effect": "none"}
    # 代码碎片
    code = random.choice(PICKUPS["code_fragments"])
    return {"name": "代码碎片", "desc": code["text"], "effect": code.get("effect", "none")}

